-------------映射
c={}
c["hello"]=2
c[3]=false

print(c.hello,c[3])
--不叫  下标  映射里面叫key
--映射中字符串不用加双引号(false)
--简化写法
d={["hello"]=2,[3]=false,}
--注意[3]=false,后面的逗号必不可少
print(d.hello,d[3])

